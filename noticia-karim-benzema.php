<h1>Karim Benzema é o melhor do mundo</h1>
<h5>O atacante do Real Madrid ficou em primeiro na premiação de melhor do mundo da France Football</h5>
<h6>Autor: Rian Garcia Pereira</h6>
<img src="./contents/karim-benzema-bola-de-ouro.jpg" alt="">
<p>Na segunda-feira dia 17 de outubro tivemos a premiação da France Football, e o principal prêmio é a bola de ouro do melhor jogador do mundo. Para você que não sabe, a bola de ouro é um prêmio da France Football entregado para o melhor jogador daquela temporada, levando em conta gols, assistências e títulos.</p>
<img src="./contents/bola-de-ouro.jpeg" alt="">
<p>O ganhador da bola de ouro da temporada 2021/2022 foi o atacante Karim Benzema, do time Real Madrid, que ganhou a LaLiga, Supercopa da Espanha e da grande UEFA Champions League. Benzema teve 46 jogos e 44 gols e 15 assistências. Ele foi o jogador mais velho a ganhar a bola de ouro depois de Stanley Matthews, que foi o vencedor em 1956, o ano de estreia desse prêmio.</p>
<p>Na cerimônia, Karim Benzema foi com uma roupa que aparentava homenagear Tupac Shakur, um famoso rapper americano, ambos com um acessório no pescoço e armação de óculos parecidos. A roupa que foi comparada com a de Tupac, foi usada em 1996 no American Music Awards em Los Angeles, alguns meses antes de sua morte. Benzema já se disse um fã de rap.</p>
